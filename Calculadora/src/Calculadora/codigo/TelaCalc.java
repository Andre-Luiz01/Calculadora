    public boolean conferencia(){
        char funsao[] = jTextFieldResultado.getText().toCharArray();
        if(!jTextFieldResultado.getText().equals("")&& funsao[funsao.length-1] != 'x' && funsao[funsao.length-1] != '/' && funsao[funsao.length-1] != '+' && funsao[funsao.length-1] != '-' && funsao[funsao.length-1] != '.' && funsao[funsao.length-1] != '^'){
            return(true);
        }else{
            return(false);
        }  
    }
    
