    private void jTextFieldResultadoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldResultadoKeyTyped
        char e = evt.getKeyChar();
        switch(e){
            case '0' :
                jButton00.doClick();
                break;
            case '1' :
                jButton01.doClick();
                break;
            case '2' :
                jButton02.doClick();
                break;
            case '3' :
                jButton03.doClick();
                break;
            case '4' :
                jButton04.doClick();
                break;
            case '5' :
                jButton05.doClick();
                break;
            case '6' :
                jButton06.doClick();
                break;
            case '7' :
                jButton07.doClick();
                break;
            case '8' :
                jButton08.doClick();
                break;
            case '9' :
                jButton09.doClick();
                break;
            case '+' :
                jButtonSoma.doClick();
                break;
            case '-' :
                jButtonMenos.doClick();
                break;
            case 'x':
                jButtonVezes.doClick();
                break;
            case '*' : 
                jButtonVezes.doClick();
                break;
            case '/' :
                jButtonDiv1.doClick();
                break;
            case '^':
                jButtonPotencia.doClick();
                break;
            case ',' :
                jButtonPonto.doClick();
                break;
            case '.' :
                jButtonPonto.doClick();
                break;
            case 'r':
                jButtonRaiz.doClick();
                break;
        }
    }//GEN-LAST:event_jTextFieldResultadoKeyTyped

    private void jTextFieldResultadoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldResultadoKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_BACK_SPACE){
            jButtonApagar.doClick();
        }else{
            if(evt.getKeyCode() == KeyEvent.VK_ENTER){
                jButtonResultado.doClick();
            }else{
                if(evt.getKeyCode() == KeyEvent.VK_ESCAPE){
                    jButtonClear.doClick();
                }
            }
        }
        
    }//GEN-LAST:event_jTextFieldResultadoKeyPressed

