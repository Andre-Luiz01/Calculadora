    private void jButtonApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApagarActionPerformed
        // TODO add your handling code here:
        if(!jTextFieldResultado.getText().equals("")){
            char funsao[] = jTextFieldResultado.getText().toCharArray();
                if(funsao[funsao.length-1] == '+'){
                    for(int i = 0; i < funsao.length-1; i++){
                        BarraRes += funsao[i];
                    }
                    jTextFieldResultado.setText(BarraRes);
                    BarraRes = "";
                    ContCaracTotal--;
                }else{
                    if(funsao[funsao.length-1] == '.'){
                        ponto = false;
                     }
                    for(int i = 0; i < funsao.length-1; i++){
                        BarraRes += funsao[i];
                    }
                    jTextFieldResultado.setText(BarraRes);
                    BarraRes = "";
                    ContCaracTotal--;


                    if(Atual.equals("")){
                        indice--;
                        if(vetor[indice].equals("x") || vetor[indice].equals("d") || vetor[indice].equals("p") || vetor[indice].equals("r")){
                            vetor[indice] = "";
                            if(indice>0){
                                Atual = vetor[indice-1];
                                char funsaoAtual[] = Atual.toCharArray();
                                for(int i = 0; i < funsaoAtual.length-1; i++){
                                    if(funsaoAtual[i] == '.'){
                                        ponto = true;
                                    }
                                }
                                
                            }
                            Atual = "";
                        }else{
                            Atual = vetor[indice];
                            char funsaoAtual[] = Atual.toCharArray();
                            ContCaracAtual = funsaoAtual.length-1;
                            for(int i = 0; i < funsaoAtual.length-1; i++){
                                BarraRes += funsaoAtual[i];
                            }
                            Atual = BarraRes;
                            BarraRes = "";
                            vetor[indice] = "";
                        }
                    }else{
                        char funsaoAtual[] = Atual.toCharArray();
                        for(int i = 0; i < funsaoAtual.length-1; i++){
                            BarraRes += funsaoAtual[i];
                        }
                        Atual = BarraRes;
                        BarraRes = "";
                        ContCaracAtual--;
                    }
                }
            
        }
        jTextFieldResultado.requestFocus();
    }//GEN-LAST:event_jButtonApagarActionPerformed

